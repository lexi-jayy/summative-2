// Get the button that opens the modal
var btn = document.getElementById('view');

// Get the <span> element that closes the modal
var span = document.getElementsByClassName('close')[0];

// When the user clicks the button, open the modal 
btn.onclick = function() {
    modal.style.display = 'block';
}

// When the user clicks on <span> (x), close the modal
span.onclick = function() {
    modal.style.display = 'none';
}


function addProductToPage(singleProduct){
   var productDiv = document.createElement("div");
    productDiv.className = "col-6 col-md-4 col__card card";

    productDiv.innerHTML += "<h2 class='card__accomodationtype'>" + singleProduct.accomodationname + "</h2>";

    productDiv.innerHTML += "<img class='card__img' src='images/" + singleProduct.image + "'>";
    productDiv.innerHTML += "<p class='card__price'> $ " + singleProduct.price + "</p>";
    productDiv.innerHTML += "<p class='card__people'>" + singleProduct.people + "</p>";
    productDiv.innerHTML += "<p class='card__nights'>" + singleProduct.nights + "</p>";
    productDiv.innerHTML += "<div class='btn btn-lg' id='view' data-toggle='modal' data-target='#modal'>" + singleProduct.text + "</div>";
   
    productDiv.innerHTML += '</div>';

   productsContainer.appendChild(productDiv);
}








